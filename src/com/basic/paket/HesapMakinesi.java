public class HesapMakinesi {
	
	public static int topla(int sayi1, int sayi2) {
		return sayi1 + sayi2;
	}
	
	public static int topla(int sayi1, int sayi2, int sayi3) {
		return sayi1 + sayi2;
	}
	
	public static int topla(double sayi1, double sayi2) {
		return (int) (sayi1 + sayi2);
	}

}
